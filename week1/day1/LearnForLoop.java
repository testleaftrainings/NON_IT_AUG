package week1.day1;

public class LearnForLoop {

	public static void main(String[] args) {
		// 1-10
		
		//syntax for->ctrl+space
		for (int i = 1; i <=10; i++) {
			System.out.println(i);
		}
		
		System.out.println("*************************");

		
		for (int i = 10; i >=1; i--) {
			System.out.println(i);
		}
	}

}
